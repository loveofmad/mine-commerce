// 创建轮播图的脚本
const fs = require('fs');
const path = require('path');

// 基础PNG文件头 (1x1 像素)
const basePng = Buffer.from([
    0x89, 0x50, 0x4E, 0x47, 0x0D, 0x0A, 0x1A, 0x0A,
    0x00, 0x00, 0x00, 0x0D,
    0x49, 0x48, 0x44, 0x52,
    0x00, 0x00, 0x00, 0x01,
    0x00, 0x00, 0x00, 0x01,
    0x08, 0x06,
    0x00, 0x00, 0x00,
    0x1F, 0x15, 0xC4, 0x89,
    0x00, 0x00, 0x00, 0x0A,
    0x49, 0x44, 0x41, 0x54,
    0x78, 0x9C, 0x62, 0x00, 0x00, 0x00, 0x02, 0x00, 0x01,
    0xE5, 0x27, 0xDE, 0xFC,
    0x00, 0x00, 0x00, 0x00,
    0x49, 0x45, 0x4E, 0x44,
    0xAE, 0x42, 0x60, 0x82
]);

const staticDir = path.join(__dirname, 'static');

// 创建轮播图
const banners = ['banner1.png', 'banner2.png', 'banner3.png'];
banners.forEach(name => {
    const bannerPath = path.join(staticDir, name);
    fs.writeFileSync(bannerPath, basePng);
    console.log(`Created: ${name}`);
});

// 创建默认头像
const avatarPath = path.join(staticDir, 'default-avatar.png');
fs.writeFileSync(avatarPath, basePng);
console.log('Created: default-avatar.png');

console.log('\nAll images created!');
